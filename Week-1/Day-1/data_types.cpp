#include <bits/stdc++.h>
using namespace std;

int main() {
    int a = 15;
    long long b = 10000000000000LL;
    double c = 3.145896;
    char d = 'S';
    bool e = true;

    cout << "a = " << a << "\n";
    cout << "b = " << b << "\n";
    cout << "c = " << c << "\n";
    cout << "d = " << d << "\n";
    cout << "e = " << e << "\n";
    
    return 0;
}